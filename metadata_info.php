            <h3>Erläuterung der Konvolutdarstellung</h3>

            <p>Bei der Konvolutdarstellung handelt sich um eine abstrakte Sicht auf den Überlieferungsträger. Dieser wird in Gänze im Profil gezeigt, um den Aufbau des Konvoluts nachvollziehbar zu machen. Das Modell wurde aus Musik-Editionen entlehnt, in denen man sich solcher Darstellungen als etablierter Vermittlungsform für die Materialanalyse schon seit geraumer Zeit bedient.</p>

            <img src="img/convolute_view.png" style="text-align: center;" alt="Beispiel einer Konvolutdarstellung" title="Beispiel einer Konvolutdarstellung">

            <p>Die waagerechten Linien stellen die Blätter einer Handschrift dar. Sie sind durchnummeriert, beginnend mit 1 (für Seite 1 usw.) und werden dann in ungerader Zählung fortgeführt (aus Platzgründen wird nur die ungerade Seite angegeben). Die obere Kante einer Linie stellt die Vorderseite eines Blattes dar, analog dazu bildet die untere Kante die Rückseite.</p>

            <p>Die Konvolutdarstellung ist von oben nach unten zu lesen - man blättert vom ersten zum letzten Blatt ‚virtuell‘ durch das Konvolut, gleich dem Durchblättern einer Handschrift im Archiv.</p>

            <p>Ein senkrechter Strich am linken Rand bezeichnet die gebundene Form eines Konvoluts. Im obigen Beispiel sind alle beiden Blätter der Handschrift gebunden, da sie ein Doppelblatt bilden. Die Art der Bindung (ob Heftung oder Klebung) wird bei dieser Darstellung nicht berücksichtigt; diese Angaben sind stattdessen der Handschriftenbeschreibung zu entnehmen.</p>

            <p>Bewegt man nun den Zeiger über die Konvolutansicht, wird neben der Illustration das dazugehörige Faksimile der Handschriftenseite parallel eingeblendet. So erhält der Nutzer eine genaue Orientierung im Handschriftenkonvolut. Beim Ansteuern der Vorderseite von Blatt 1 (Seite 1) würde zugleich das Faksimile der ersten Seite der Handschrift präsentiert. Beim Ansteuern der Fläche zwischen den Linien, beispielsweise derjenigen, die mit 1 und 3 bezeichnet sind (s. o.), werden zwei Handschriftenseiten eingeblendet: S. 2 (die Rückseite des ersten Blattes) und S. 3 (die erste Seite des zweiten Blattes), wie man sie gleichermaßen beim Öffnen der Handschrift im Archiv vorfinden würde. Ein aufleuchtender grauer Balken zeigt dem Nutzer, wo er sich gerade im Konvolut befindet. Möchte man den Animationsmodus verlassen und wünscht eine statische Ansicht, erhält man diese mit einem Klick - erkennbar ist der statische Modus daran, dass der Balken zur Orientierung im Konvolut nun rot dargestellt wird (s. o.).</p>

            <p>Ein Klick auf das Faksimile ruft die Handschriftenansicht der gewählten Seite auf.</p>

