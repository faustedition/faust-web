            <h2>Beschreibung der Überlieferungsträger</h2>
            <h3>Handschriftenbeschreibung</h3>

            <p>Für jeden Textzeugen existiert eine Beschreibung, in der grundlegende Informationen zur jeweiligen Handschrift enthalten sind. Die Anordnung der Daten erfolgt hierarchisch: Beim ersten Zugang zur Handschrift werden die wichtigsten Informationen, die für die Identifikation der Handschrift und ihre werkgeschichtliche Einordnung von Bedeutung sind, dem Nutzer bereitgestellt. Sie beziehen sich allesamt auf den Überlieferungszeugen in seiner Gesamtheit.</p>
            <p>Zur ausführlichen Information über die Beschaffenheit einer Handschrift in ihrer Gänze (Dokumenteigenschaften) kann die Anzeige der Metadaten um Details zu den Eigenschaften einzelner Handschriftenblätter, des Papiers, einzelner Seiten (und eventuell an diesen vorzufindenden Anbringungen/Zetteln) erweitert werden. Alle verfügbaren Metadaten einer Handschrift stehen als Download zur Verfügung bereit.</p>
            <p>Die Angaben wurden in der Regel formelhaft vollzogen, um ihre Auswertbarkeit zu gewährleisten. Wo dies nicht geschah, wird im Folgenden explizit auf die Verwendung von Freitext hingewiesen. Manche Angaben sind in Klammern gesetzt; dies bedeutet, dass bei diesen Kriterien die Datenerhebung fakultativ war. In allen anderen ist sie entsprechend obligatorisch.</p>
            <p>Nicht vorhandene Merkmale werden ebenso in der Handschriftenbeschreibung vermerkt („none“). Können keine genauen Aussagen getroffen werden, beispielsweise aufgrund der schlechten Papierqualität, die das Erkennen eines Wasserzeichens verhindert, das aber durchaus vorhanden sein kann, wird dies mit dem Hinweis „n.s.“ (für „not specified“) verzeichnet.</p>
            <p>Bei redundanten Informationen (z. B. der Papiersorte, die in einem bestimmten Konvolut durchgängig identisch ist), wird die entsprechende Information jeweils an oberster Stelle angetragen und 'vererbt' sich auf alle darunterliegenden Ebenen - außer, es wird im Folgenden explizit eine abweichende Angabe gemacht, die die erste aufhebt (wenn beispielsweise ein Blatt in einem Konvolut aus anderem Papier als die übrigen Blätter besteht).</p>

            <p>Im Folgenden sind zunächst die wichtigsten Kriterien der Handschriftenbeschreibung (Punkt 1) aufgeführt. Es folgen die ihnen nachgelagerten weiterführenden Informationen (Punkt 2).</p>
