        <p>Anhand von Wasserzeichen können Hypothesen zu Handschriftendatierungen überprüft werden:
            Ein Vergleich genutzter Papiere lässt Rückschlüsse auf die Entstehungsbedingungen des
            Werks zu; bei der Rekonstruktion historischer Konvolutstrukturen von Handschriften etwa
            können Wasserzeichen wichtige Hinweise liefern.</p>
        <p>Im Fall eines so umfangreichen Korpus wie dem von Goethes „Faust“ eröffnen sich für den
            Nutzer der Ausgabe mit der Bereitstellung von Informationen zu Wasserzeichen neue
            Möglichkeiten der genetischen Analyse. Neben den inzwischen in einer
            historisch-kritischen Edition nicht mehr wegzudenkenden Wasserzeicheninformationen
            werden in dieser Ausgabe auch Wasserzeichenaufnahmen zur Verfügung gestellt. Aufgrund
            der Menge an überlieferten Handschriften konnte dies nur in Auswahl geschehen,
            prototypisch wurde das im Goethe- und Schiller-Archiv Weimar (GSA) befindliche
            Handschriftenmaterial zum „Faust“ aus der frühen Zeit (bis 1808) hierfür herangezogen.
            Es konnten aus Gründen des Bestandsschutzes nur Handschriften in ungebundener Form
            berücksichtigt werden, Konvolute wurden ausgeschieden.</p>
