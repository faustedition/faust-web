        <h3>Beschreibung der Wasserzeichen</h3>
        <p>Zu jedem Wasserzeichen erfolgte eine Beschreibung, und zwar nicht nur für diejenigen, zu
            denen Aufnahmen angefertigt wurden, sondern zu allen in der Edition versammelten
            Handschriften.</p>
        <h2>Benennungssystem der Wasserzeichendigitalisate</h2>
        <p>Die Dateinamen wurden als 'sprechende' semantisch gefüllt. So verraten sie zugleich
            wichtige Detailinformationen zu den Aufnahmebedingungen. Sie setzen sich wie folgt
            zusammen:</p>
        <p>Den ersten Teil des Dateinamens bildet die Signaturangabe gemäß der in der Edition
            üblichen Schreibweise: GSA_25-W_…</p>
        <p>Es folgt entweder die Abkürzung <em>wm</em> (für <em>watermark</em>) für die
            Kennzeichnung der Wasserzeichenaufnahmen oder <em>hand_drawn</em> für die manuell
            angefertigten Durchzeichnungen.</p>
        <p>Sodann wird unterschieden in <em>all</em> (für die Aufnahme der gesamten Seite) und in
                <em>detail</em> für einen spezifischen Ausschnitt der Seite, auf dem das
            Wasserzeichen zu erkennen ist.</p>
        <p>Wo notwendig, werden als ergänzende Information recto- (<em>r</em>) und verso-Angaben
                (<em>v</em>) bereitgestellt.</p>
        <p>Gespiegelte Aufnahmen werden mit <em>flipped</em> gekennzeichnet.</p>
        <p>Die dpi-Zahl wird im Dateinamen der JPEG-Bilder angetragen (da sie dort differieren
            kann), bei den TIFF-Dateien geschieht dies nicht, da alle über 300 dpi verfügen.</p>
        <p>Die Zahl am Ende (100, 150) gibt den ISO-Wert (Lichtempfindlichkeit des Bildsensors)
            an.</p>
        <p>Die Kombination <em>TL</em> und Zahl (28 bzw. 30) kommt nur bei der Handschrift GSA 25/W
            1589 vor, die für die ersten Probeläufe verwendet wurde; <em>TL</em> steht für
            Durchlichtaufnahmen (<em>transmitted light</em>); die Zahl zeigt an, welche der
            Durchlichtdateien der Subtraktion zugrunde lag.</p>
        <h2>Kooperationspartner</h2>
        <p>An der Reproduktion der Wasserzeichen, ihrer Bearbeitung und Beschreibung waren mehrere
            Institutionen und Personen beteiligt: Das GSA stellte die Handschriften bereit sowie die
            Technik und Räumlichkeiten, Carmen Knickmeier, Mitarbeiterin des GSA, übernahm die Auf-
            und Durchlichtaufnahmen. Dr. Georg Dietz (PAPIERSTRUKTUR, Dresden) gab Expertise bei den
            Aufnahmen, bearbeitete die Bilder und fertigte die Durchzeichnungen der Wasserzeichen
            an. Das Team der Papierhistorischen Sammlungen des Deutschen Buch- und Schriftmuseums
            der Deutschen Nationalbibliothek Leipzig wirkte beratend beim Projekt mit (ein
            gemeinsames Treffen erfolgte am 30. Januar 2013 in Leipzig); Andrea Lothe, dortige
            Mitarbeiterin, half bei der Identifizierung der Papiere und fertigte die Beschreibung
            der Wasserzeichen an. Finanziert wurden die Aufnahmen und ihre Bearbeitung vom Lehrstuhl
            für Computerphilologie und Neuere Deutsche Literaturgeschichte (Prof. Dr. Fotis
            Jannidis) der Universität Würzburg.</p>
        <h2>Ausblick</h2>
        <p>Die Bereitstellung von Wasserzeichenaufnahmen soll exemplarisch aufzeigen, welche
            Interpretationsmöglichkeiten sich mit der Einbindung dieser Materialien in eine digitale
            Edition für den Nutzer bieten. Eine systematische Wasserzeichenanalyse konkret von
            „Faust“- wie generell von Goethe-Papieren steht indes noch aus. Sie kann auf zahlreiche
            grundlegende Vorarbeiten, maßgeblich der Akademie-Ausgabe, den 1910 begonnenen
            Wasserzeichenkatalog in Weimar (Goethe-Nationalmuseum) und dessen Weiterführung durch
            Gerhard Femmel sowie auf Einzelstudien, maßgeblich von David Lee, aufbauen.</p>
