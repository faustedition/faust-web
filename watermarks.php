            Handschriftenblätter angefertigt. Dies geschah in oktagonaler Anordnung, Kamera- und
            Objektebene durften nicht verkantet sein. Es wurde stets die gesamte Seite eines
            Handschriftenblatts (Einzel- wie auch Doppelblatt) aufgenommen. Beleuchtet wurden die
            Objekte von unten mittels Leuchttisch. Als Vorderseite wurde in der Regel die
            Handschriftenseite gewählt, wo das Wasserzeichen klar 'von vorn' zu erkennen ist, die
            Beschriftungsreihenfolge und -anordnung spielte hingegen keine Rolle.</p>
