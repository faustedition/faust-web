            <h3>Zeilensynoptische Darstellung</h3>
            <p>
              Mehrfach überlieferte Passagen des Lesetextes sind farblich unterlegt. Die unterschiedliche Färbung verrät den Grad der Varianz: Je dunkler eine Stelle markiert ist, desto mehr Varianten weist sie auf. Der Grad der Abweichung wirkt sich hingegen nicht auf die Unterlegung aus. Die Varianzeinfärbung dient als Orientierung für das Aufspüren genetisch aufschlussreicher Passagen im Text.
              <br>
              <img alt="" src="img/Varianzeinfaerbung.png" height="186" width="386">
              <br>
            </p>
            <p>
              Die Einzelzeilensynopse ist im Lesetext von Faust&nbsp;I bzw. Faust&nbsp;II verankert.
              <br>
              Für jeden Vers, auch auch für jede Überschrift, Sprecherbezeichnung, Bühnenanweisung usw. ist eine chronologisch geordnete Liste der Fassungen abrufbar, in denen das betreffende Textsegment überliefert ist. Die am Rand stehende Kurzbezeichnung führt zur Einzeldarstellung des betreffenden Zeugen.
              <br>
              Handschriften und Drucke werden, wenn möglich, mit den gebräuchlichsten Siglen bezeichnet. Im Fall der Handschriften sind dies die von Erich Schmidt in den Faust-Bänden der Weimarer Ausgabe eingeführten Siglen. Tragen Handschriften dort keine Sigle, treten die von der späteren Faust-Forschung vergebenen Bezeichnungen oder die in der Akademie- und der Weimarer Ausgabe (WA) eingeführten Siglen ein. Steht keine Sigle zur Verfügung, wird auf die Archivsignatur zurückgegriffen. Wenn es sich nicht um eine von Schmidt herrührende Sigle handelt, wird die Herkunft der Bezeichnung ausgewiesen.
              <br>
              Drucke werden mit den durch Waltraud Hagen eingeführten Siglen bezeichnet. Trägt ein Druck dort keine Sigle, werden die durch die WA eingeführten Siglen, ersatzweise die Archivsignatur verwendet. Die Bezeichnungen, die nicht von Hagen herrühren, werden als solche explizit gekennzeichnet.
            </p>
