        <h2>Handschriften</h2>
        <p>Goethe hat beinahe in jeder Phase seines Lebens an dem Werkprojekt ‚Faust‘ gearbeitet.
            Erhalten ist ein umfangreicher Bestand an Handschriften, die von etwa 1774 bis 1831
            reichen.</p>
        <p>Die erhaltenen Handschriften bestehen aus Notizen, Entwürfen zu einzelnen Szenen und
            Versgruppen, Arbeitshandschriften, vorläufigen und überarbeiteten Reinschriften sowie
            Druckvorlagen. Der größte Teil der Überlieferung gehört zum ‚Faust II‘ und stammt aus
            der Zeit seit der Wiederaufnahme der Arbeit am ‚Faust‘ im Jahr 1825. Der weitaus
            umfangreichste Teil der Textzeugen liegt im Goethe- und Schiller-Archiv in Weimar (GSA).
            Der Bestand des GSA umfasst 429 Archivalieneinheiten mit insgesamt 1639 beschriebenen
            Seiten zum ‚Faust‘. 64 Handschriften mit ca. 295 beschriebenen Seiten befinden sich im
            Besitz von 20 weiteren Institutionen in Deutschland, der Schweiz, Großbritannien, Polen,
            Frankreich, Italien und in den Vereinigten Staaten.</p>
        <p>Das <a href="archive.php">digitale Archiv</a> macht die gesamte relevante Überlieferung
            des ‚Faust‘ in Abbildungen zugänglich. Der handschriftliche Zusammenhang der Blätter,
            Bogen und Lagen wird in der Konvolutdarstellung wiedergegeben. Jede Handschrift ist mit
            einer detaillierten Beschreibung versehen (<a href="metadata.php">Metadaten</a>).</p>
        <p>Den Abbildungen stehen zwei Transkriptionen zur Seite, die <a
                href="transcription_guidelines.php#dok_Transkr_Hss">dokumentarische</a> und die <a
                href="transcription_guidelines.php#txt_Transkr_Hss">textuelle</a>. Die
            dokumentarische Transkription gibt die Niederschrift in ihren räumlichen Verhältnissen
            genau wieder. Sie dient der Erschließung der oftmals schwer entzifferbaren
            Handschriften. Die textuelle Transkription abstrahiert vom Erscheinungsbild der
            Handschriften und gibt den Text einer Handschrift wieder.</p>

        <h2>Drucke</h2>
        <p>1790 erschien ‚Faust. Ein Fragment‘ im Druck. 1808 erschien in der ersten bei Cotta
            erschienenen Gesamtausgabe ‚Faust. Eine Tragödie‘ mit „Der Tragödie erster Theil“. Zwei
            Partien des zweiten Teils wurden 1827 und 1828 in der Ausgabe letzter Hand
            veröffentlicht: ‚Helena, klassisch- romantische Phantasmagorie. Zwischenspiel zu Faust‘
            sowie, unter dem Titel ‚Faust. Zweyter Theil‘, der spätere Beginn des ersten Akts. Zu
            einer vollständigen Veröffentlichung des ‚Faust II‘ kam es erst kurz nach Goethes Tod,
            1833.</p>        
        <p>Teil der Ausgabe sind alle textkritisch relevanten <a
                href="archive.php?view=print-concordance">Drucke</a> und Teildrucke des ‚Faust‘.
            Dazu gehören neben den Drucken im Rahmen von Gesamtausgaben, den Einzel- und
            Journaldrucken auch handschriftlich bearbeitete Korrekturexemplare und
            Revisionsbogen.</p>
        <p>Zu jedem Druck ist eine Transkription sowie eine Beschreibung verfügbar.</p>

        <h2>Lesetext</h2>
        <p>Die Lesetexte des ‚Faust I‘ und ‚Faust II‘ sind eine getreue, unemendierte Wiedergabe des
            jeweils zugrundeliegenden Zeugen. Für ‚Faust I‘ ist dies der Druck im achten Band der
            ersten bei Cotta erschienenen Gesamtausgabe (der sogenannten Ausgabe A). Dem Text des
            ‚Faust II‘ liegt die 187 Blätter umfassende sogenannte große Reinschrift zugrunde
            (WA-Sigle H bzw. in unserer Ausgabe 2 H). Bei der Wiedergabe gelten dieselben Prinzipien
            wie bei den <a href="transcription_guidelines.php#txt_Transkr_Hss">textuellen
                Transkriptionen</a>. Im Unterschied zu den übrigen Textwiedergaben sind die
            Seitengrenzen der Vorlage im Lesetext nicht markiert.</p>
        <p>Die Lesetexte dienen als Bezugstexte für die Verszählung und die genetische Darstellung.
            Durch Auswahl der einzelnen Zeilen können alle überlieferten Fassungen eines Verses,
            einer Überschrift, einer Sprecherbezeichnung, einer Bühnenanweisung etc. in
            zeilensynoptischer Anordnung aufgerufen werden. Die Sigle am rechten Rand führt zur
            Einzeldarstellung des Zeugen im digitalen Archiv.</p>

        <h2>Genese</h2>
        <p>Verschiedene Grafiken stellen die Entstehung von Goethes ‚Faust‘ retrospektiv, d.h. vom
            abgeschlossenen Werk ausgehend dar.</p>
        <h3>Gesamtübersicht</h3>
        <p>In der linken Hälfte der <a href="genesis.php">Gesamtübersicht</a> sind all
            jene Werkteile versammelt, die im Jahr 1808 unter dem Titel ‚Faust. Eine Tragödie‘
            erschienen sind. In der rechten Hälfte findet sich eine Übersicht zum ‚Faust&nbsp;II‘.
            In beiden Hälften entsprechen die angetragenen Jahreszahlen entstehungsgeschichtlich
            relevanten, an Arbeitsphasen orientierten Daten.</p>
        <p>Die verschiedenen Einfärbungen der Felder zeigen den Ausarbeitungsgrad an. Weiß bedeutet,
            dass der betroffene Textabschnitt zu dieser Zeit nicht vorliegt. Grau indiziert
            Unabgeschlossenheit, Schwarz Fertigstellung.</p>
        <p>Die Auswahl einer Spalte in einer der beiden Hälften führt zu einer detaillierteren
            Darstellung.</p>
        <h3>Übersichten zum ‚Faust&nbsp;I‘ und zum ‚Faust&nbsp;II‘</h3>
        <p>Die Übersicht zum <a href="genesis_faust_i.php">‚Faust&nbsp;I‘</a> ist nach Szenen
            gegliedert. Die einleitenden Abschnitte ‚Zueignung‘, ‚Vorspiel‘ und ‚Prolog‘ bleiben
            unbeziffert, die nachfolgenden Szenen werden durchgezählt. Beim Ansteuern einer Spalte
            erscheint der Szenentitel.</p>
        <p>Die Übersicht zum <a href="genesis_faust_ii.php">‚Faust&nbsp;II‘</a> ist nach Akten
            gegliedert, die mit römischen Ziffern durchgezählt sind. Einzelne Spalten entsprechen Szenen,
            deren Titel beim Ansteuern erscheint. Die vor 1825 konzipierten oder
            entstandenen Szenen sind in der unteren Zeile zusammengefasst.</p>
        <h3>Balkendiagramm zu einzelnen Szenen</h3>
        <p>Die Auswahl einer Spalte in den Übersichten zum ‚Faust&nbsp;I‘ und ‚Faust&nbsp;II‘ führt
            zu einem Balkendiagramm, in dem alle relevanten Handschriften und Drucke zu der
            betreffenden Szene angezeigt werden. Im Diagramm sind neben handschriftlichen und
            gedruckten Fassungen auch Paralipomena und Schemata berücksichtigt. Damit werden auch
            skizzenartige und stichworthafte Niederschriften ohne direkte Entsprechung im Werktext
            erfasst.</p>
        <p>Die Balken zeigen an, welchem Abschnitt des Werktextes ein Zeuge entspricht.
            Handschriftliche Fassungen sind blau, Drucke schwarz, Paralipomena grün und Schemata
            violett dargestellt. Lücken in einem Balken bedeuten, dass der Zeuge an dieser Stelle
            keine Entsprechung zum Werktext aufweist. Helle Farbabstufungen drücken bei den
            Paralipomena und Schemata Unsicherheit in der Zuordnung, bei den handschriftlichen
            Fassungen hohen Abweichungsgrad aus.</p>
        <p>Die Verszahlen des Werktextes sind von links nach rechts angetragen, die Siglen der
            Zeugen in chronologischer Folge von unten nach oben. Explizit macht das Diagramm nur
            Angaben zur chronologischen Ordnung von Fassungen, die gemeinsamen Text aufweisen.</p>
        <p>Beim Ansteuern eines Balkens werden das zugehörige Versintervall und die
            Überlieferungsform (Handschrift, Druck, Paralipomenon, Schema) angezeigt. Die Auswahl
            eines Balkens führt zur entsprechenden Stelle im Textzeugen.</p>

        <h2>Siglen</h2>
        <p>In den tabellarischen Übersichten des Archivs, den Ansichten einzelner Handschriften und
            Drucke, der zeilensynoptischen Darstellung im Lesetext sowie im Balkendiagramm finden
            Kurzbezeichnungen für Zeugen, sog. Siglen, Verwendung. Diese basieren auf dem
            System der Siglierung, das Erich Schmidt, der Herausgeber des ‚Faust‘ in den Bänden 14
            und 15 der Weimarer Ausgabe (WA), für die Handschriften des ‚Faust&nbsp;II‘ eingeführt
            hat. Die Handschriften werden dabei nach der Texterstreckung geordnet.
            Maßgebend ist nicht der Umfang einer Handschrift, sondern die Stelle im Werktext,
            mit der eine Handschrift einsetzt. Eine Handschrift, die mit einem früheren Abschnitt
            einsetzt, wird vor einer Handschrift eingeordnet, die mit einem späteren Vers
            einsetzt.</p>

        <h3>Siglen der Handschriften zum ‚Faust&nbsp;I‘</h3>
        <p>Zum Faust&nbsp;I liegen vergleichsweise wenige Handschriften vor, zu jeder Szene meist
            nur eine. 
            Erich Schmidt hat die Handschriften deshalb nicht fortlaufend numeriert; stattdessen spricht 
            er einfach jeweils von „H“ als der
            Handschrift zu der betreffenden Szene (WA&nbsp;I&nbsp;14); die Göchhausensche Abschrift
            des ‚Urfaust‘ bezeichnet er mit „U“. 
            Diese Praxis wird in der Faust-Edition nicht fortgesetzt. 
            Um eindeutige Kurzbezeichnungen für jede
            Handschrift zu erhalten, werden die Handschriften zum ‚Faust&nbsp;I‘ in der
            Faust-Edition durchlaufend numerisch nach dem Prinzip der Texterstreckung
            sigliert. </p>

        <h3>Siglen der Handschriften zum ‚Faust&nbsp;II‘</h3>
        <p>Der Großteil der Siglen zu den einzelnen Akten des ‚Faust&nbsp;II‘ wurde von Erich
            Schmidt gemäß dem oben beschriebenen Prinzip der Texterstreckung eingeführt. Seit dem
            Erscheinen von Bd.&nbsp;15.2 der WA neu aufgefundene Handschriften wurden teils schon
            von Erich Schmidt durch Hinzufügung neuer Siglen in dieses System integriert. Um alle ihm
            noch unbekannten oder aus anderen Gründen nicht siglierten Handschriften mit einer
            eindeutigen Kurzbezeichnung versehen zu können, ist ein Teil der Siglen Erich Schmidts
            wie auch der späteren Faust-Forschung ersetzt worden:</p>
        <ol>
            <li>Die von Schmidt für die außerhalb des Goethe- und Schiller-Archivs befindlichen Handschriften eingeführte
                alphabetische Siglierung ließ sich nicht so fortsetzen, dass dabei das Prinzip der
                Texterstreckung hätte aufrechterhalten werden können. Alle betreffenden Handschriften sind
                in die Reihe der numerisch siglierten Handschriften eingefügt worden. 
                Dazu wurden die neue numerische Siglen mit einer Buchstabenerweiterung eingeführt, beginnend mit
                I&nbsp;H.0d für die Handschrift mit der WA-Sigle I&nbsp;H.a. Im Prinzip ist so schon Renate Fischer-Lamberg
                (faust://bibliography/fischer-lamberg1955) verfahren.</li>
            <li>Einige der von Schmidt unter einer gemeinsamen Sigle zusammengefassten Handschriften
                liegen heute unter verschiedenen Archivsignaturen oder sogar an verschiedenen
                Aufbewahrungsorten. Um einzelne archivalische Objekte eindeutig referenzierbar zu machen,
                wurden die betreffenden Siglen mit einem Zusatz erweitert.</li>
        </ol>
        <p>Die von Erich Schmidt eingeführten numerischen Siglen blieben dagegen immer erhalten, auch
            wenn sie gegen das sonst geltende Prinzip der Texterstreckung verstoßen (z.B.
            <a href="/documentViewer.php?faustUri=faust://xml/document/faust/2.2/gsa_391247.xml">II&nbsp;H.74</a>).</p>

        <h3>Siglen der Drucke</h3>
        <p>Drucke erhalten die durch Waltraud Hagen (faust://bibliography/hagen1983), ersatzweise
            die durch die WA eingeführte Sigle.</p>

