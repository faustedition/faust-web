        <h4>1.3. Konvolutdarstellung</h4>
        <p>Die Konvolutdarstellung ist eine schematisierte
        Gesamtansicht des Überlieferungsträgers (in der Abbildung
        rechts). Mit ihrer Hilfe lassen sich Handschriften leicht
        durchblättern.</p><img alt="" src="img/IH32struktur.PNG"
        height="304" width="624" />
        <p>Waagerechte Linien entsprechen Blättern. Die obere Kante
        der Linie stellt die Vorderseite dar, die untere die
        Rückseite.</p>
        <p>Die Seiten werden einheitlich und unabhängig von
        vorhandenen Zählungen (Foliierung, Paginierung)
        durchgezählt. Nur die Zählung der Vorderseiten wird
        angezeigt.</p>
        <p>Im Beispiel entsprechen die Seiten 1 bis 2 einem freien
        Einzelblatt, die Seiten 3, 4, 9, 10 und 5 bis 8 jeweils
        einem Doppelblatt. Das zweite Doppelblatt ist in das erste
        eingelegt und bildet mit ihm eine Lage.<br /></p>
        <p>Beim Ansteuern der Konvolutansicht werden links die
        zugehörigen Faksimiles eingeblendet. Rote Farbe zeigt die
        letzte aktivierte Seitenansicht an. Aktivierbare
        Seitenpaare sind grau unterlegt. Die Auswahl einer
        Faksimileseite ruft die jeweilige Handschriftenansicht
        auf.</p>
