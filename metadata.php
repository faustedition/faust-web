      <h2 class="c1">Beschreibung der Überlieferungsträger
      (Metadaten)<br /></h2>
      <div>
        <h3 class="c2">1. Handschriftenbeschreibung</h3>
        <p>Zu jeder einzelnen Handschrift gibt es eine Beschreibung
        mit Angaben (Metadaten) zu Siglen, Aufbewahrungsort,
        Überlieferung, Blatt- und Papiereigenschaften sowie
        Konvolutstruktur, die in der Strukturansicht angezeigt
        werden. Sie ist unterteilt in einen kürzeren Teil mit
        grundlegenden Angaben zur gesamten Handschrift und ihrer
        Zuordnung zum Faust-Text und einen zusätzlich einblendbaren
        längeren Teil mit ausführlichen Detailinformationen.</p>
        <p>Zwischen einem negativen Befund und dem Vermeiden einer
        Angabe wird in der Anzeige nicht unterschieden, aber in den
        zugrundeliegenden Dateien wird die Differenzierung beachtet
        („none“ für negativen Befund versus „n.s.“ = „not
        specified“).</p>
      </div>
