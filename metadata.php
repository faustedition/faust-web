      <h3 id="Drucke_Meta">2. Metadaten der Drucke</h3>
      <p>Zu jedem Druck gibt es eine Beschreibung mit Angaben
      (Metadaten) zur Sigle des Drucks in der maßgeblichen
      Bibliographie von Waltraud Hagen und weiteren Siglen, Nummer
      des Drucks bei Hagen, gegebenenfalls zum Status als
      Korrekturexemplar oder Revisionsbogen. Bei
      Korrekturexemplaren und Revisionsbögen werden auch
      Aufbewahrungsort und Archivsignatur angegeben.</p>
      <p>Es folgt die Titelaufnahme mit Angabe des Formats.</p>
      <p>Aufbewahrungsort und Bibliothekssignatur der
      digitalisierten sowie weiterer herangezogener Exemplare
      werden nachgewiesen.</p>
      <p>Die Literaturangaben am Schluss umfassen:</p>
      <ul>
        <li>beschreibende Nachweise des Drucks in
        wissenschaftlichen Ausgaben und in der
        Forschungsliteratur;</li>
        <li>wissenschaftliche Ausgaben, deren Text auf dem
        betreffenden Druck beruht;</li>
        <li>photomechanische Nachdrucke;</li>
        <li>Angaben und Erörterungen über den stemmatologischen Ort
        eines Drucks in wissenschaftlichen Ausgaben und in der
        Forschungsliteratur.</li>
      </ul><br />
