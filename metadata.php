        <p>Fakultative Angaben stehen in Klammern.</p>
        <ul>
          <li>Dokumenteigenschaften
            <ul>
              <li>(Überlieferung/Provenienz)</li>
              <li>(Historische Aufbewahrungsform) – Zeitgenössische
              Behälter, z. B. Kassetten oder Kapseln, werden
              ausführlicher beschrieben, etwa mit Angaben zu
              Entstehungszeit, Stärke, Material und Form.</li>
              <li>(Einband)</li>
              <li>(Vorgefundene Zählungen) – Z.&#160;B. Foliierung
              oder Paginierung.</li>
              <li>(Erhaltungszustand der Handschrift)</li>
            </ul>
          </li>
          <li>Blatteigenschaften
            <ul>
              <li>Blattmaße – Gemessen werden Breite × Höhe in
              Millimetern. Bei ungleichmäßig geschnittenen Papieren
